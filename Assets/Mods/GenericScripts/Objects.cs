

public class Tuple<T, T1>
{
	public T A;
	public T1 B;

	public Tuple(T a, T1 b)
	{
		this.A = a;
		this.B = b;
	}
}


public class Truple<T, T1, T2>
{
	public T A;
	public T1 B;
	public T2 C;

	public Truple(T a, T1 b, T2 c)
	{
		this.A = a;
		this.B = b;
		this.C = c;
	}
}
